package oops.ClassObjectInheritancesOverriding;

// Base class Animal
public class Animal {
    // Instance variables
    String colour = "blue";
    int age = 12;

    // Method to simulate eating
    public void eating() {
        System.out.println("Animal is eating");
    }

    // Method to simulate sleeping
    public void sleep() {
        System.out.println("Animal is sleeping");
    }

    // Method to simulate speaking
    public void speak() {
        System.out.println("Animal makes a sound");
    }
}
