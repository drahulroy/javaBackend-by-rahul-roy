package oops.ClassObjectInheritances;

// Base class Animal
public class Animal {
    // Instance variables
    String colour = "blue";
    int age = 12;

    // Method to simulate eating
    public void eating() {
        System.out.println("Eating");
    }

    // Method to simulate sleeping
    public void sleep() {
        System.out.println("Animal is sleeping");
    }
}
