package in.four7.demo.game;

public class MarioGame {
    public void up(){
        System.out.println("Mario will jump");
    }
    public void down() {
        System.out.println("Mario will sit");
    }
    public void left() {
        System.out.println("Mario will stop");
    }
    public void right() {
        System.out.println("Mario will run");
    }
}
